var ul = document.getElementById("ul");
var ulAppend = (data) => {
  ul.innerHTML = null;
  data.forEach((ele) => {
    let li = document.createElement("li");
    li.innerText = ele.li;
    li.addEventListener("click", function () {
      temFilter(li.innerText);
    });
    ul.append(li);
  });
};
